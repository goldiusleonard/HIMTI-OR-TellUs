
public abstract class Character {
	
	public abstract long getLevel();
	
	public abstract long getMaxHealth();
	
	public abstract long getHealth();
	
	public abstract long getAtk();
	
	public abstract long getDef();
	
	public abstract void setLevel(long level);
	
	public abstract void setMaxHealth(long maxHealth);
	
	public abstract void setHealth(long health);
	
	public abstract void setAtk(long atk);
	
	public abstract void setDef(long def);
	
	public abstract void setDefaultStat();
	
}
